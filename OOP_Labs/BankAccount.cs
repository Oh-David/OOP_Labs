using System;
using System.Collections.Generic;
using System.Text;

namespace OOP_Labs
{
    public class BankAccount
    {
        private int _id;
        private decimal _balance;

        public int Id
        {
            get
            {
                return _id;
            }
            set
            {
                _id = Id;
            }
        }

        public decimal Balance
        {
            get
            {
                return _balance;
            }
            set
            {
                _balance = Balance;
            }
        }
    }
}
